export const HALF_LIFE_DAYS = 7;
const MS_PER_DAY = 86_400_000;

const DEFAULT_TOP_KEYWORDS = 20;
const DEFAULT_TOP_ARCS = 10;
const DEFAULT_PROVENANCE_DAYS = 7;
const MAX_RECENT_ACTIVITY_ENTRIES = 50;

export type PrimerScope = "vault" | "mesh" | "federation";

export interface PrimerGenerateArgs {
  scope: PrimerScope;
  scopeTarget?: string;
  topKeywords?: number;
  topArcs?: number;
  provenanceDays?: number;
  // Test / determinism seam. Defaults to new Date().toISOString().
  nowIso?: string;
  // When true, the flow renders + returns markdown but skips disk write.
  dryRun?: boolean;
  // Open-once seam (v1.A.5 CR-B1 pattern).
  registryDb?: Client;
}

export interface PrimerKeyword {
  keyword: string;
  // Score = sum-over-lanes(mem_count × exp(-days_since_last_built / HALF_LIFE_DAYS)).
  score: number;
  // Sum of mem_count across all lanes containing this keyword.
  totalMemCount: number;
  // ISO of the most recent last_built across lanes carrying this keyword.
  lastSeen: string;
}

export interface PrimerArc {
  name: string;
  category: string;
  lastTouched: string;
  vaultName: string;
  memberCount: number;
}

export interface PrimerActivity {
  ts: number;
  tsIso: string;
  targetType: string;
  targetId: string;
  src: string;
  vaultName: string;
  // Hex of the UUIDv7 BLOB primary key — used as a deterministic tie-breaker.
  idHex: string;
}

export interface PrimerLane {
  name: string;
  keywords: readonly string[];
  memCount: number;
  vaultName: string;
}

export interface PrimerGenerateResult {
  scope: PrimerScope;
  scopeTarget: string | null;
  primerPath: string;
  markdown: string;
  dryRun: boolean;
  vaultsScanned: readonly string[];
  topKeywords: PrimerKeyword[];
  topArcs: PrimerArc[];
  recentActivity: PrimerActivity[];
  topLanes: PrimerLane[];
  durationMs: number;
}

export async function generatePrimerFlow(args: PrimerGenerateArgs): Promise<PrimerGenerateResult> {
  const startedAt = Date.now();
  const nowIso = args.nowIso ?? new Date().toISOString();
  const nowMs = Date.parse(nowIso);
  if (Number.isNaN(nowMs)) {
    throw new Error(
      `primer-generator: invalid nowIso ${JSON.stringify(args.nowIso)} (not parseable as ISO 8601).`,
    );
  }
  const topKeywords = clampPositive(args.topKeywords ?? DEFAULT_TOP_KEYWORDS);
  const topArcs = clampPositive(args.topArcs ?? DEFAULT_TOP_ARCS);
  const provenanceDays = clampPositive(args.provenanceDays ?? DEFAULT_PROVENANCE_DAYS);
  const dryRun = args.dryRun === true;
  const provenanceCutoffMs = nowMs - provenanceDays * MS_PER_DAY;

  const callerSuppliedRegistry = args.registryDb !== undefined;
  const registryDb = args.registryDb ?? (await openRegistry());

  let perVault: VaultAggregates[];
  let writeAnchorVault: VaultRow;
  let primerFileName: string;
  try {
    const resolved = await resolveScopeContext(registryDb, args.scope, args.scopeTarget);
    if (resolved.vaults.length === 0) {
      throw new Error(
        `primer-generator: scope=${args.scope}${args.scopeTarget !== undefined ? ` --target ${args.scopeTarget}` : ""} resolved 0 vaults. Register at least one vault first via 'lyt vault init'.`,
      );
    }
    writeAnchorVault = resolved.writeAnchor;
    // hardening pass (fix-pass): the primer WRITE lands in the anchor vault's
    // .lyt/primers — gate the mutation at its destination (any scope).
    // dry-run primers stay available on a frozen vault (read-only).
    if (!dryRun) {
      await enforceNotFrozen(writeAnchorVault.path, writeAnchorVault.name);
    }
    primerFileName = `${args.scope}-primer.md`;
    perVault = await aggregatePerVault(resolved.vaults, provenanceCutoffMs);
  } finally {
    if (!callerSuppliedRegistry) await closeRegistry(registryDb);
  }

  const merged = mergeAggregates(perVault, nowMs);
  const topKeywordList = merged.keywords.slice(0, topKeywords);
  const topArcsList = merged.arcs.slice(0, topArcs);
  const recentActivityList = merged.recentActivity.slice(0, MAX_RECENT_ACTIVITY_ENTRIES);
  const topLanesList = merged.lanes.slice(0, topArcs);

  const scopeLabel = formatScopeLabel(args.scope, args.scopeTarget, writeAnchorVault);
  const isFederationStub = args.scope === "federation";

  const markdown = renderPrimerMarkdown({
    scope: args.scope,
    scopeLabel,
    writeAnchorVault,
    nowIso,
    provenanceDays,
    topKeywords: topKeywordList,
    keywordsAreFallback: merged.keywordsAreFallback,
    topArcs: topArcsList,
    recentActivity: recentActivityList,
    topLanes: topLanesList,
    isFederationStub,
    scopeTarget: args.scopeTarget,
  });

  const primersDir = join(writeAnchorVault.path, ".lyt", "primers");
  const primerPath = join(primersDir, primerFileName);
  if (!dryRun) {
    mkdirSync(primersDir, { recursive: true });
    atomicWriteFile(primerPath, markdown);
  }

  return {
    scope: args.scope,
    scopeTarget: args.scopeTarget ?? null,
    primerPath,
    markdown,
    dryRun,
    vaultsScanned: perVault.map((p) => p.vault.name),
    topKeywords: topKeywordList,
    topArcs: topArcsList,
    recentActivity: recentActivityList,
    topLanes: topLanesList,
    durationMs: Date.now() - startedAt,
  };
}

// ---------------------------------------------------------------------------
// Scope resolution
// ---------------------------------------------------------------------------

interface ResolvedScope {
  vaults: VaultRow[];
  // The vault whose .lyt/primers/ directory receives the file.
  writeAnchor: VaultRow;
}

async function resolveScopeContext(
  registryDb: Client,
  scope: PrimerScope,
  scopeTarget: string | undefined,
): Promise<ResolvedScope> {
  switch (scope) {
    case "vault": {
      if (scopeTarget === undefined) {
        throw new Error("primer-generator: scope=vault requires --target <vault-name>.");
      }
      const v = await getVaultByName(registryDb, scopeTarget);
      if (!v) {
        throw new Error(`primer-generator: no vault registered with name '${scopeTarget}'.`);
      }
      return { vaults: [v], writeAnchor: v };
    }
    case "mesh": {
      if (scopeTarget === undefined) {
        throw new Error("primer-generator: scope=mesh requires --target <mesh-name>.");
      }
      const mesh: MeshRow | null = await getMeshByName(registryDb, scopeTarget);
      if (!mesh) {
        throw new Error(`primer-generator: no mesh registered with name '${scopeTarget}'.`);
      }
      const all = await listVaults(registryDb);
      const homeMatch = all.filter(
        (v) => v.homeMeshRid !== null && equalBytes(v.homeMeshRid, mesh.rid),
      );
      const memberRows = await listVaultsInMesh(registryDb, mesh.rid);
      const memberByHex = new Set(memberRows.map((r) => r.vaultRidHex));
      const memberVaults = all.filter((v) => memberByHex.has(v.ridHex));
      const byHex = new Map<string, VaultRow>();
      for (const v of homeMatch) byHex.set(v.ridHex, v);
      for (const v of memberVaults) byHex.set(v.ridHex, v);
      const meshVaults = [...byHex.values()].sort(byNameAsc);
      const writeAnchor = await resolveMeshWriteAnchor(registryDb, mesh, meshVaults);
      return { vaults: meshVaults, writeAnchor };
    }
    case "federation": {
      // OD-4 default — federation-repo not shipped (v1.A.0 pending);
      // emit per-vault stub at first-vault-by-name with explicit warning.
      const all = [...(await listVaults(registryDb))].sort(byNameAsc);
      if (all.length === 0) {
        return {
          vaults: [],
          // Synthetic empty anchor — caller will throw on 0 vaults.
          writeAnchor: {
            rid: new Uint8Array(),
            ridHex: "",
            name: "",
            path: "",
            status: "active",
          } as unknown as VaultRow,
        };
      }
      return { vaults: all, writeAnchor: all[0]! };
    }
  }
}

async function resolveMeshWriteAnchor(
  registryDb: Client,
  mesh: MeshRow,
  meshVaults: readonly VaultRow[],
): Promise<VaultRow> {
  // Prefer the mesh's main vault when registered.
  if (mesh.mainVaultRid !== null) {
    const main = await getVaultByRid(registryDb, mesh.mainVaultRid);
    if (main !== null) return main;
  }
  // Fall back to the first member by name.
  if (meshVaults.length > 0) return meshVaults[0]!;
  throw new Error(
    `primer-generator: mesh '${mesh.name}' has no member vaults to write the primer into.`,
  );
}

// ---------------------------------------------------------------------------
// Per-vault aggregation
// ---------------------------------------------------------------------------

interface VaultAggregates {
  vault: VaultRow;
  lanes: LaneRow[];
  arcs: ArcRow[];
  arcMemberCountByRidHex: Map<string, number>;
  // Lane V 0.4 (V-F9): lane rid hex → most-recent member figment authored
  // (frontmatter modified) ISO. Decay recency reads this, not lane build-time.
  laneRecencyByRidHex: Map<string, string>;
  recentProvenance: PrimerActivity[];
  // V-C-1 SC3 option-b: per-figment topic/tags keyword signals, consumed ONLY
  // when no lane formed (the empty-keywords fallback).
  keywordSignals: KeywordSignal[];
}

async function aggregatePerVault(
  vaults: readonly VaultRow[],
  provenanceCutoffMs: number,
): Promise<VaultAggregates[]> {
  const out: VaultAggregates[] = [];
  for (const vault of vaults) {
    if (vault.status === "tombstoned") continue;
    // corrupt lyt.db → CorruptLytDbError naming `lyt reindex`, not raw.
    const lytDb = await openLytDbActionable(vault.path, vault.name);
    let provDb: Client | null = null;
    try {
      const lanes = await listLanes(lytDb);
      const arcs = await listArcs(lytDb);
      const arcMemberCountByRidHex = new Map<string, number>();
      for (const arc of arcs) {
        // listArcs already returned the rid; count its members via the same
        // open lytDb client to keep this single-connection per vault.
        const memberCount = await countArcMembers(lytDb, arc.rid);
        arcMemberCountByRidHex.set(bytesToHexLower(arc.rid), memberCount);
      }

      // Lane V 0.4 (V-F9) — per-lane authored recency = the most recent member
      // figment's frontmatter `modified` (via figment_meta + lane_members).
      // Keyword decay reads THIS, not lane build-time, so a fresh tag outranks
      // a stale one regardless of when the cache was last rebuilt.
      const modifiedByPath = await loadModifiedByPath(lytDb);
      // V-C-1 SC3 option-b: load the per-figment topic/tags keyword signals up
      // front (one table scan) — used only if the lane-derived keyword list is
      // empty (mergeAggregates decides; cheap to always load at v1 vault sizes).
      const keywordSignals = await loadKeywordSignals(lytDb);
      const laneRecencyByRidHex = new Map<string, string>();
      for (const lane of lanes) {
        const members = await listMembersByLane(lytDb, lane.rid);
        let maxIso: string | null = null;
        for (const m of members) {
          const iso = modifiedByPath.get(m.figmentPath);
          if (iso !== undefined && (maxIso === null || iso > maxIso)) maxIso = iso;
        }
        if (maxIso !== null) laneRecencyByRidHex.set(bytesToHexLower(lane.rid), maxIso);
      }

      provDb = await openProvenanceDb(vault.path);
      const provenanceActivity = await loadRecentProvenance(provDb, provenanceCutoffMs, vault.name);
      // Lane V 0.4 (V-F16) — recent activity also includes recently-AUTHORED
      // figments (frontmatter `modified` within the window): the authored-time
      // source the (empty) event ledger could not provide.
      const cutoffIso = new Date(provenanceCutoffMs).toISOString();
      const recentFigments = await listRecentFigments(
        lytDb,
        cutoffIso,
        MAX_RECENT_ACTIVITY_ENTRIES,
      );
      const figmentActivity: PrimerActivity[] = recentFigments.map((rf) => ({
        ts: Date.parse(rf.modifiedIso),
        tsIso: rf.modifiedIso,
        targetType: "note",
        targetId: rf.figmentPath,
        src: "figment",
        vaultName: vault.name,
        idHex: `fm:${rf.figmentPath}`,
      }));
      const recentProvenance = [...provenanceActivity, ...figmentActivity];

      out.push({
        vault,
        lanes,
        arcs,
        arcMemberCountByRidHex,
        laneRecencyByRidHex,
        recentProvenance,
        keywordSignals,
      });
    } finally {
      await closeVaultDb(lytDb);
      if (provDb !== null) await closeVaultDb(provDb);
    }
  }
  return out;
}

async function countArcMembers(lytDb: Client, arcRid: Uint8Array): Promise<number> {
  const res = await lytDb.execute({
    sql: "SELECT COUNT(*) AS n FROM arc_members WHERE arc_rid = ?",
    args: [arcRid],
  });
  const row = res.rows[0];
  if (row === undefined) return 0;
  return Number(row["n"] as number | bigint);
}

async function loadRecentProvenance(
  provDb: Client,
  cutoffMs: number,
  vaultName: string,
): Promise<PrimerActivity[]> {
  const res = await provDb.execute({
    sql:
      "SELECT id, ts, target_type, target_id, src FROM provenance" +
      " WHERE ts >= ? ORDER BY ts DESC LIMIT ?",
    args: [cutoffMs, MAX_RECENT_ACTIVITY_ENTRIES],
  });
  return res.rows.map((row) => {
    const ts = Number(row["ts"] as number | bigint);
    return {
      ts,
      tsIso: new Date(ts).toISOString(),
      targetType: String(row["target_type"]),
      targetId: String(row["target_id"]),
      src: String(row["src"]),
      vaultName,
      idHex: uuid7BytesToHex(row["id"] as Uint8Array | ArrayBuffer),
    };
  });
}

// ---------------------------------------------------------------------------
// Cross-vault merge + scoring
// ---------------------------------------------------------------------------

interface MergedAggregates {
  keywords: PrimerKeyword[];
  // V-C-1 SC3 option-b: true when `keywords` came from the figment topic/tags
  // FALLBACK (no lane formed), so the renderer can mark the section instead of
  // implying a real lane signal. False for the normal lane-derived path AND for
  // a genuinely empty pod (0 figments → 0 fallback signals → honest placeholder).
  keywordsAreFallback: boolean;
  arcs: PrimerArc[];
  recentActivity: PrimerActivity[];
  lanes: PrimerLane[];
}

function mergeAggregates(perVault: readonly VaultAggregates[], nowMs: number): MergedAggregates {
  // Keywords: per-keyword aggregate across all lanes across all vaults.
  interface KwAcc {
    score: number;
    totalMemCount: number;
    // ISO of the most recent last_built across contributing lanes (string
    // ASC comparison is correct for ISO 8601 timestamps).
    lastSeenIso: string;
  }
  const kwAcc = new Map<string, KwAcc>();
  for (const v of perVault) {
    for (const lane of v.lanes) {
      // Lane V 0.4 (V-F9): recency = the lane's most-recent member AUTHORED
      // time (frontmatter `modified`) when known, else fall back to cache
      // build-time. Reading authored-time makes a fresh tag outrank a stale
      // one — previously both decayed off the (identical) build timestamp.
      const recencyIso = v.laneRecencyByRidHex.get(bytesToHexLower(lane.rid)) ?? lane.lastBuilt;
      const recencyMs = Date.parse(recencyIso);
      const daysSinceLast = Number.isFinite(recencyMs)
        ? Math.max(0, (nowMs - recencyMs) / MS_PER_DAY)
        : 0;
      const recencyFactor = Math.exp(-daysSinceLast / HALF_LIFE_DAYS);
      const contribution = lane.memCount * recencyFactor;
      for (const k of lane.sourceKeywords) {
        const existing = kwAcc.get(k);
        if (existing === undefined) {
          kwAcc.set(k, {
            score: contribution,
            totalMemCount: lane.memCount,
            lastSeenIso: recencyIso,
          });
        } else {
          existing.score += contribution;
          existing.totalMemCount += lane.memCount;
          if (recencyIso > existing.lastSeenIso) {
            existing.lastSeenIso = recencyIso;
          }
        }
      }
    }
  }
  const keywords: PrimerKeyword[] = [];
  for (const [keyword, acc] of kwAcc) {
    keywords.push({
      keyword,
      score: acc.score,
      totalMemCount: acc.totalMemCount,
      lastSeen: acc.lastSeenIso,
    });
  }
  keywords.sort((a, b) => {
    if (a.score !== b.score) return b.score - a.score;
    return a.keyword < b.keyword ? -1 : a.keyword > b.keyword ? 1 : 0;
  });

  // V-C-1 SC3 option-b — keyword FALLBACK. When NO lane formed (the empty
  // keyword list), derive keywords from each figment's frontmatter topic/tags
  // (figment_meta) so a single/untagged capture still surfaces a topical signal.
  // The fallback NEVER overrides a non-empty lane signal (no V-C-1 regression):
  // it is gated strictly on `keywords.length === 0`. A genuinely empty pod
  // (0 figments → 0 signals) keeps the honest empty placeholder.
  let keywordsAreFallback = false;
  let outKeywords = keywords;
  if (keywords.length === 0) {
    const fbAcc = new Map<
      string,
      { figmentCount: number; fromTopic: boolean; lastSeenIso: string | null }
    >();
    for (const v of perVault) {
      for (const sig of v.keywordSignals) {
        const cur = fbAcc.get(sig.keyword);
        if (cur === undefined) {
          fbAcc.set(sig.keyword, {
            figmentCount: sig.figmentCount,
            fromTopic: sig.fromTopic,
            lastSeenIso: sig.lastSeenIso,
          });
        } else {
          cur.figmentCount += sig.figmentCount;
          cur.fromTopic = cur.fromTopic || sig.fromTopic;
          if (
            sig.lastSeenIso !== null &&
            (cur.lastSeenIso === null || sig.lastSeenIso > cur.lastSeenIso)
          ) {
            cur.lastSeenIso = sig.lastSeenIso;
          }
        }
      }
    }
    if (fbAcc.size > 0) {
      const fbEntries = [...fbAcc.entries()].map(([keyword, a]) => ({ keyword, ...a }));
      // Same determinism contract as the lane path: count DESC, topic-ahead-on-
      // ties (topic is the stronger signal), keyword ASC. Byte-identical output
      // for the same vault state.
      fbEntries.sort((a, b) => {
        if (a.figmentCount !== b.figmentCount) return b.figmentCount - a.figmentCount;
        if (a.fromTopic !== b.fromTopic) return a.fromTopic ? -1 : 1;
        return a.keyword < b.keyword ? -1 : a.keyword > b.keyword ? 1 : 0;
      });
      outKeywords = fbEntries.map((e) => ({
        keyword: e.keyword,
        score: e.figmentCount,
        totalMemCount: e.figmentCount,
        // No nowIso here (keeps mergeAggregates pure + deterministic on vault
        // state alone); an em-dash when no contributing figment carried a date.
        lastSeen: e.lastSeenIso ?? "—",
      }));
      keywordsAreFallback = true;
    }
  }

  // Arcs.
  const arcs: PrimerArc[] = [];
  for (const v of perVault) {
    for (const arc of v.arcs) {
      arcs.push({
        name: arc.name,
        category: arc.category,
        lastTouched: arc.lastTouched,
        vaultName: v.vault.name,
        memberCount: v.arcMemberCountByRidHex.get(bytesToHexLower(arc.rid)) ?? 0,
      });
    }
  }
  arcs.sort((a, b) => {
    if (a.lastTouched !== b.lastTouched) return a.lastTouched < b.lastTouched ? 1 : -1;
    return a.name < b.name ? -1 : a.name > b.name ? 1 : 0;
  });

  // Recent activity.
  const recentActivity: PrimerActivity[] = [];
  for (const v of perVault) {
    for (const e of v.recentProvenance) recentActivity.push(e);
  }
  recentActivity.sort((a, b) => {
    if (a.ts !== b.ts) return b.ts - a.ts;
    return a.idHex < b.idHex ? -1 : a.idHex > b.idHex ? 1 : 0;
  });

  // Lanes.
  const lanes: PrimerLane[] = [];
  for (const v of perVault) {
    for (const lane of v.lanes) {
      lanes.push({
        name: lane.name,
        keywords: lane.sourceKeywords,
        memCount: lane.memCount,
        vaultName: v.vault.name,
      });
    }
  }
  lanes.sort((a, b) => {
    if (a.memCount !== b.memCount) return b.memCount - a.memCount;
    return a.name < b.name ? -1 : a.name > b.name ? 1 : 0;
  });

  return { keywords: outKeywords, keywordsAreFallback, arcs, recentActivity, lanes };
}

