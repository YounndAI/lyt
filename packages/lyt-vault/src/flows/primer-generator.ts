// v1.D.4 — primer-generator flow.
//
// Composes a deterministic agent-priming markdown file from already-shipped
// per-vault caches: lanes.db (v1.D.1b) + arcs.db (v1.D.2b) + provenance.db
// (v1.A.2c per-vault DB SPLIT). Query-LESS aggregation (unlike v1.D.3 search
// which is query-driven): top-N keywords by frequency × recency, active
// arcs by last_touched DESC, recent provenance (last N days), top lanes
// by mem_count.
//
// Scope semantics (mirrors search-cascade.ts):
// vault — single registered vault; writes to <vault>/.lyt/primers/vault-primer.md
// mesh — union of home_mesh_rid + mesh_vaults entries; writes to
// mesh's main vault's .lyt/primers/mesh-primer.md
// federation — per OD-4 default: federation-repo not shipped yet
// (v1.A.0 pending) so we emit a per-vault stub at
// <first-vault-by-name>/.lyt/primers/federation-primer.md
// with a `> [!warning]` callout noting the limitation.
// Full federation-repo write lands in v1.A.0 + v1.D.4d.
//
// Recency weighting (OD-6 default): per keyword, score = mem_count ×
// exp(-days_since_lane.last_built / HALF_LIFE_DAYS). HALF_LIFE_DAYS = 7
// is the named constant. The formula is exponential decay with a 7-day
// characteristic time (1/e ≈ 0.37 after 7 days); the constant name is
// retained verbatim from OD-6 phrasing even though the formula is NOT a
// strict mathematical half-life (which would require ln(2)/7 in the
// exponent). The unit test asserts "a recent tag outranks an older tag
// at same base count" — that property holds for any monotonic-decreasing
// weighting; choosing exp() over linear preserves room for future tuning.
//
// Markdown shape (OD-7 default — 5 sections + Generated by footer):
// # Primer: <scope-name>
// <!-- Generated by ... -->
// ## Vault identity
// ## Top keywords
// ## Active arcs
// ## Recent activity (last <N> days)
// ## Top lanes
// ---
// ## Generated by
//
// Atomic write (OD-8 default): tmp file in same directory → rename(2).
// `--dry-run` returns the rendered markdown in the result and SKIPS disk
// write entirely.
//
// Lock 0.3 deterministic output (OD-10): pin `nowIso` via the test seam +
// sort all aggregates deterministically (keywords: score DESC, keyword
// ASC; arcs: last_touched DESC, name ASC; provenance: ts DESC, idHex
// ASC; lanes: mem_count DESC, name ASC). Same vault state + same nowIso
// → byte-identical markdown.

