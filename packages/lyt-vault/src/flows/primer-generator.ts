interface RenderArgs {
  scope: PrimerScope;
  scopeLabel: string;
  writeAnchorVault: VaultRow;
  nowIso: string;
  provenanceDays: number;
  topKeywords: readonly PrimerKeyword[];
  keywordsAreFallback: boolean;
  topArcs: readonly PrimerArc[];
  recentActivity: readonly PrimerActivity[];
  topLanes: readonly PrimerLane[];
  isFederationStub: boolean;
  scopeTarget: string | undefined;
}

function renderPrimerMarkdown(args: RenderArgs): string {
  const lines: string[] = [];
  lines.push(`# Primer: ${args.scopeLabel}`);
  lines.push("");
  lines.push(`<!-- Generated by \`lyt primer\` at ${args.nowIso}. Regenerate to refresh. -->`);
  lines.push("");

  if (args.isFederationStub) {
    lines.push("> [!warning] Federation-repo not shipped yet (v1.A.0 pending).");
    lines.push(
      "> This file is a per-vault stub written into the first-vault-by-name's `.lyt/primers/`.",
    );
    lines.push(
      "> Full federation aggregation across `~/lyt/pod/primers/` lands in v1.A.0 + v1.D.4d.",
    );
    lines.push("");
  }

  // ## Vault identity
  lines.push("## Vault identity");
  lines.push("");
  lines.push(`- **Vault**: ${args.writeAnchorVault.name}`);
  lines.push(
    `- **Scope**: ${args.scope}${args.scopeTarget !== undefined ? ` (--target ${args.scopeTarget})` : ""}`,
  );
  lines.push(`- **Generated**: ${args.nowIso}`);
  lines.push("");

  // ## Top keywords
  lines.push("## Top keywords");
  lines.push("");
  if (args.topKeywords.length === 0) {
    lines.push(
      "_(No keywords yet — run `lyt vault rebuild-lanes` to populate `.lyt/indexes/lanes.yon` first.)_",
    );
  } else {
    // V-C-1 SC3 option-b: when no lane formed, keywords come from the figment
    // topic/tags fallback — mark the section so the signal isn't mistaken for a
    // real lane cluster. (≥2 figments must share a tag to form a lane.)
    if (args.keywordsAreFallback) {
      lines.push(
        "_(Derived from figment topic/tags — no lanes yet; ≥2 figments must share a tag to form a lane.)_",
      );
      lines.push("");
    }
    lines.push("| Keyword | Score | Total mem | Last seen |");
    lines.push("|---|---|---|---|");
    for (const k of args.topKeywords) {
      lines.push(`| ${k.keyword} | ${formatScore(k.score)} | ${k.totalMemCount} | ${k.lastSeen} |`);
    }
  }
  lines.push("");

  // ## Active arcs
  lines.push("## Active arcs");
  lines.push("");
  if (args.topArcs.length === 0) {
    lines.push(
      "_(No arcs yet — run `lyt vault rebuild-arcs` to populate `.lyt/indexes/arcs.yon` first.)_",
    );
  } else {
    lines.push("| Arc | Category | Last touched | Members | Vault |");
    lines.push("|---|---|---|---|---|");
    for (const a of args.topArcs) {
      lines.push(
        `| ${a.name} | ${a.category} | ${a.lastTouched} | ${a.memberCount} | ${a.vaultName} |`,
      );
    }
  }
  lines.push("");

  // ## Recent activity (last N days)
  lines.push(`## Recent activity (last ${args.provenanceDays} days)`);
  lines.push("");
  if (args.recentActivity.length === 0) {
    lines.push(
      "_(No provenance entries in this window — `provenance.db` is empty or all entries are older than the window.)_",
    );
  } else {
    lines.push("| Time | Target | Source | Vault |");
    lines.push("|---|---|---|---|");
    for (const r of args.recentActivity) {
      lines.push(
        `| ${r.tsIso} | ${r.targetType}:${truncate(r.targetId, 60)} | ${r.src} | ${r.vaultName} |`,
      );
    }
  }
  lines.push("");

  // ## Top lanes
  lines.push("## Top lanes");
  lines.push("");
  if (args.topLanes.length === 0) {
    lines.push(
      "_(No lanes yet — run `lyt vault rebuild-lanes` to populate `.lyt/indexes/lanes.yon` first.)_",
    );
  } else {
    lines.push("| Lane | Keywords | Members | Vault |");
    lines.push("|---|---|---|---|");
    for (const l of args.topLanes) {
      lines.push(`| ${l.name} | ${l.keywords.join(", ")} | ${l.memCount} | ${l.vaultName} |`);
    }
  }
  lines.push("");

  lines.push("---");
  lines.push("");
  lines.push("## Generated by");
  lines.push("");
  const cmdParts = [`lyt primer --scope ${args.scope}`];
  if (args.scopeTarget !== undefined) cmdParts.push(`--target ${args.scopeTarget}`);
  lines.push(`\`${cmdParts.join(" ")}\` at ${args.nowIso}`);
  lines.push("");
  return lines.join("\n");
}

function formatScopeLabel(
  scope: PrimerScope,
  scopeTarget: string | undefined,
  writeAnchor: VaultRow,
): string {
  if (scopeTarget !== undefined) return scopeTarget;
  if (scope === "federation") return "federation";
  return writeAnchor.name;
}

function formatScore(n: number): string {
  if (n === 0) return "0";
  if (n >= 100) return n.toFixed(0);
  if (n >= 10) return n.toFixed(1);
  return n.toFixed(2);
}

function truncate(s: string, n: number): string {
  if (s.length <= n) return s;
  return s.slice(0, n - 1) + "…";
}

