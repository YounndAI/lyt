// v1.D.5 — bumped 2 → 3 with the addition of the LYT_PRIMER section
// (agents.md `## Vault primer` between LYT_PRIMER_BEGIN/END markers) and
// the `## Vault Primer` transclusion in lyt-overview.md. Older v2 agents.md
// files without LYT_PRIMER markers stay untouched on regen (no surprise-
// edit) — only NEW files (via `getAgentsMdContent` / `scaffold/init.ts`)
// get the full v3 template.
export const AGENTS_MD_TEMPLATE_VERSION = 3;

export const MESH_CONTEXT_AUTO_BANNER =
  "<!-- AUTO-REGENERATED by `lyt vault regen-context`. Edits to this file are overwritten. -->";

// Delimiters marking the auto-regenerated "Installed patterns + skills" section in
// agents.md. The regen flows (pattern link/unlink, vault sync-metadata --apply)
// replace ONLY the content between these markers; the rest of agents.md is user-
// editable. Matches the priming-file transclusion pattern from 7A.
export const AGENTS_MD_PATTERNS_BEGIN = "<!-- LYT_PATTERNS_BEGIN -->";
export const AGENTS_MD_PATTERNS_END = "<!-- LYT_PATTERNS_END -->";

// v1.D.5 — delimiters for the agents.md `## Vault primer` section that
// points handlers at the `.lyt/primers/` directory + the `lyt primer
// --scope vault` regen verb. Same regen contract as LYT_PATTERNS: only
// the content between markers is replaced. NEW agents.md files (via
// `getAgentsMdContent`) include the markers + populated section; EXISTING
// v2 files without the markers stay untouched on regen.
export const AGENTS_MD_PRIMER_BEGIN = "<!-- LYT_PRIMER_BEGIN -->";
export const AGENTS_MD_PRIMER_END = "<!-- LYT_PRIMER_END -->";

export interface LytOverviewInput {
  vaultName: string;
  desc: string | null | undefined;
  owner: string;
}

export function getLytOverviewContent(input: LytOverviewInput): string {
  const desc = (input.desc ?? "").trim();
  const lines: string[] = [
    `# ${input.vaultName}`,
    ``,
    `## What this vault is`,
    ``,
    desc.length > 0
      ? desc
      : `_(Add a description here. Lyt scaffolded this section once at \`lyt vault init\` and never touches it again — it is yours to evolve.)_`,
    ``,
    `## Owner`,
    ``,
    input.owner,
    ``,
    `## Mesh Context`,
    ``,
    `![[.lyt/mesh-context]]`,
    ``,
    // v1.D.5 — additive `## Vault Primer` section AFTER `## Mesh Context`
    // per OD-4 default. Transcludes the markdown emitted by `lyt primer
    // --scope vault` so handlers opening lyt-overview.md in Obsidian see
    // the primer's hot keywords / active arcs / recent activity / top lanes
    // inline. The transclusion target may not exist on a fresh vault — in
    // that case Obsidian renders the embed as an empty placeholder; run
    // `lyt primer --scope vault --target <vault-name>` to populate it.
    `## Vault Primer`,
    ``,
    `![[.lyt/primers/vault-primer]]`,
    ``,
  ];
  return lines.join("\n");
}

export interface InstalledPatternSummary {
  id: string;
  name: string;
  version: string;
  verbIds: readonly string[];
  // Skill slugs that wrap this pattern's verbs (e.g., ["/lyt-plan", "/lyt-retro"]).
  // Optional — when omitted, the listing shows pattern + verbs without skill mapping.
  skills?: readonly string[] | undefined;
}

export interface AgentsMdInput {
  vaultName: string;
  // OPTIONAL — when present, the "Installed patterns + skills" section is populated
  // between the LYT_PATTERNS delimiters. When omitted/empty, the section shows the
  // empty-state placeholder.
  installedPatterns?: readonly InstalledPatternSummary[] | undefined;
}

