function renderInstalledPatternsSection(
  installed: readonly InstalledPatternSummary[] | undefined,
): string {
  if (!installed || installed.length === 0) {
    return `_(No patterns linked into this vault yet. Run \`lyt pattern list\` to see what's installed at \`~/lyt/patterns/\`, then \`lyt pattern link <name> --vault <this-vault>\` to link them. Or run \`lyt vault adopt <path>\` / \`lyt vault join <path>\` — both auto-rebuild symlinks for every installed pattern.)_`;
  }
  const lines: string[] = [];
  lines.push(`This vault has the following Lyt patterns linked at \`Patterns/\`:`, ``);
  for (const p of installed) {
    const verbs = p.verbIds.join(", ");
    if (p.skills && p.skills.length > 0) {
      lines.push(`- **${p.id}@${p.version}** (verbs: ${verbs}) → skills: ${p.skills.join(", ")}`);
    } else {
      lines.push(`- **${p.id}@${p.version}** (verbs: ${verbs})`);
    }
  }
  lines.push(
    ``,
    `Run \`lyt help patterns\` for the full pattern catalog. Run \`lyt pattern verbs <name>\` for a pattern's verb list.`,
  );
  return lines.join("\n");
}

// Regenerate ONLY the LYT_PATTERNS section of an existing agents.md, leaving the rest
// of the file (which the user may have edited) intact. If the markers aren't present
// (older v1 template), the entire file is rewritten via getAgentsMdContent.
export function regenInstalledPatternsSection(
  existingContent: string,
  vaultName: string,
  installed: readonly InstalledPatternSummary[],
): string {
  const beginIdx = existingContent.indexOf(AGENTS_MD_PATTERNS_BEGIN);
  const endIdx = existingContent.indexOf(AGENTS_MD_PATTERNS_END);
  if (beginIdx < 0 || endIdx < 0 || endIdx <= beginIdx) {
    return getAgentsMdContent({ vaultName, installedPatterns: installed });
  }
  const before = existingContent.slice(0, beginIdx + AGENTS_MD_PATTERNS_BEGIN.length);
  const after = existingContent.slice(endIdx);
  const middle = "\n" + renderInstalledPatternsSection(installed) + "\n";
  return before + middle + after;
}

// v1.D.5 — render the content of the `## Vault primer` section that lives
// between the LYT_PRIMER markers. The content is a static pointer to the
// `.lyt/primers/` directory + the `lyt primer` regen verb — the actual
// primer markdown lives in `.lyt/primers/{scope}-primer.md` and is
// transcluded into lyt-overview.md via `![[.lyt/primers/vault-primer]]`.
// Putting a pointer (not the full primer text) in agents.md keeps the
// file scannable for agents while preserving the SoT in the primer file.
function renderInstalledPrimerSection(vaultName: string): string {
  return [
    `_The primer files at \`.lyt/primers/\` hold hot keywords + active arcs + recent activity + top lanes for this vault and mesh. Read them as a fast on-ramp before deeper exploration._`,
    ``,
    `Regenerate with:`,
    ``,
    `- \`lyt primer --scope vault --target ${vaultName}\` → \`.lyt/primers/vault-primer.md\``,
    `- \`lyt primer --scope mesh --target <mesh-name>\` → \`<mesh-main-vault>/.lyt/primers/mesh-primer.md\``,
    `- \`lyt primer --scope federation\` → \`<federation-or-first-vault>/.lyt/primers/federation-primer.md\``,
  ].join("\n");
}

// v1.D.5 — regenerate ONLY the LYT_PRIMER section of an existing
// agents.md. Mirror of `regenInstalledPatternsSection`. If markers are
// absent (older v2 file), return the existing content UNCHANGED — do
// NOT surprise-edit user-edited agents.md by auto-injecting the new
// section (per "What was decided" §8 of the v1.D.5 brief + global
// Rule #9 don't-add-what-wasn't-asked).
//
// To opt an existing v2 file into v3, either delete `agents.md` (next
// regen recreates from the v3 template via getAgentsMdContent) or hand-
// edit the file to include the LYT_PRIMER markers around an empty
// section (next regen populates).
export function regenInstalledPrimerSection(existingContent: string, vaultName: string): string {
  const beginIdx = existingContent.indexOf(AGENTS_MD_PRIMER_BEGIN);
  const endIdx = existingContent.indexOf(AGENTS_MD_PRIMER_END);
  if (beginIdx < 0 || endIdx < 0 || endIdx <= beginIdx) {
    return existingContent;
  }
  const before = existingContent.slice(0, beginIdx + AGENTS_MD_PRIMER_BEGIN.length);
  const after = existingContent.slice(endIdx);
  const middle = "\n" + renderInstalledPrimerSection(vaultName) + "\n";
  return before + middle + after;
}

